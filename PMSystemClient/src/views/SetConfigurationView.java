    public void configure() {
        this.translate();
        Configuration config = this.reportsController.getConfiguration();
        if(config != null) {
            jComboBox1.setSelectedItem(config.language);
            jComboBox2.setSelectedItem(config.theme);
        }
    }
    
    public void reset() {
        Configuration config = this.reportsController.getConfiguration();
        if(config != null) {
            jComboBox1.setSelectedItem(config.language);
            jComboBox2.setSelectedItem(config.theme);
        }
    }
    
