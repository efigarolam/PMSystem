    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String language = jComboBox1.getSelectedItem().toString();
        String theme    = jComboBox2.getSelectedItem().toString();
        
        String[] result = this.reportsController.setConfiguration(language, theme);
        
        this.alertsHelper.showAlert(__(result[0]), result[1]);
        
        if(result[1].equals("information")) {
            MainController.setLanguage(language);
            MainController.adminView.translate();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

