    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int sender     = MainController.userOnlineID;
        String subject = jTextField1.getText();
        String message = jTextArea1.getText();
        String date    = this.timeHelper.getActualTime();   
        String state   = "Unread";
        boolean email  = jCheckBox1.isSelected();
        
        String[] result = this.messagesController.sendMessage(sender, this.receiver, subject, message, date, state, email);
        
        this.alertsHelper.showAlert(result[0], result[1]);
    }//GEN-LAST:event_jButton1ActionPerformed

