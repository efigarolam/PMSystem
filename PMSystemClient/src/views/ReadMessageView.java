    public void configure(int id_message) {
        this.translate();
        
        MessageData message = this.messagesController.getMessage(id_message);
        
        if(message != null) {
            if(MainController.userOnlineID != message.id_sender) {
                this.messagesController.updateMessageState(message.id, "Read");
                this.receiver = message.id_sender;
                jLabel9.setText(jLabel9.getText()+" "+message.sender);
                jLabel4.setText(jLabel4.getText()+" "+message.sender);
                jTextPane2.setText(message.subject);
                jTextField1.setText("Re: "+message.subject);
                jTextField1.select(0, 0);
                jTextPane1.setText(message.message);
            } else {
                this.receiver = message.id_receiver;
                jLabel9.setText(jLabel9.getText()+" "+message.sender);
                jLabel4.setText(jLabel4.getText()+" "+message.receiver);
                jTextPane2.setText(message.subject);
                jTextField1.setText("Re: "+message.subject);
                jTextField1.select(0, 0);
                jTextPane1.setText(message.message);
            }
            
        }
    }
