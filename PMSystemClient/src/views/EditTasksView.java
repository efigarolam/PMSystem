    private void jComboBox5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox5ActionPerformed
        if(evt.getSource() == jComboBox5 && !this.preventDefault) {
            ComboBoxHelper dataTask = (ComboBoxHelper) jComboBox5.getSelectedItem();
            TaskData task = this.tasksController.getTask(dataTask.getID());
            
            //Select the project of the task from the combobox
            ProjectsBasicData[] projects = this.projectsController.getAllProjects();
            jComboBox3.removeAllItems();
            if(projects != null) {
                for(int i = 0; i < projects.length; i++) {
                    ComboBoxHelper cbbh = new ComboBoxHelper(projects[i].id, projects[i].name);
                    jComboBox3.addItem(cbbh);
                    if(task.id_project == projects[i].id) {
                        jComboBox3.setSelectedIndex(i);
                    }
                }
            }
            
            //Select the users of the task from the jlist
            UsersBasicData[] users = this.usersController.getAllUsers();
            ArrayList selected = new ArrayList();
            int[] iUsers = new int[task.users.length];
            for(int j = 0; j < users.length; j++) {
                for(int k = 0; k < task.users.length; k++) {
                    if(task.users[k].equals(users[j].username)) {
                        selected.add(j);
                    }
                }
            }
            
            for(int l = 0; l < iUsers.length; l++) {
                iUsers[l] = Integer.parseInt(selected.get(l).toString());
            }
            
            jList1.setSelectedIndices(iUsers);            
            
            //Set all the other fields
            this.ID_Task = task.id;
            jTextField1.setText(task.title);
            jTextArea1.setText(task.description);
            jDateChooser1.setDate(Date.valueOf(task.start_date));
            jDateChooser2.setDate(Date.valueOf(task.end_date));
            jSlider1.setValue(task.progress);
            jComboBox4.setSelectedItem(task.priority);
            jComboBox2.setSelectedItem(task.state);
       }
    }//GEN-LAST:event_jComboBox5ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String title       = jTextField1.getText();
        String description = jTextArea1.getText();
        String start_date  = this.timeHelper.getDateString(jDateChooser1.getDate());
        String end_date    = this.timeHelper.getDateString(jDateChooser2.getDate());
        int progress       = jSlider1.getValue();
        String priority    = jComboBox4.getSelectedItem().toString();
        String state       = jComboBox2.getSelectedItem().toString();
        
        int ID_Project;
        try {
            ComboBoxHelper dataProject = (ComboBoxHelper) jComboBox3.getSelectedItem();
            ID_Project = dataProject.getID();
        } catch(Exception e) {
            ID_Project = 0;
        }
            
        String[] users;
        Object[] listUsers;
        
        listUsers = jList1.getSelectedValues();
        users = new String[listUsers.length];
        
        for(int i = 0; i < listUsers.length; i++) {
            users[i] = listUsers[i].toString();
        }
              
        String[] result = this.tasksController.editTask(this.ID_Task, ID_Project, users, title, description, start_date, end_date, progress, priority, state);
        
        this.alertsHelper.showAlert(__(result[0]), result[1]);
    }//GEN-LAST:event_jButton1ActionPerformed

