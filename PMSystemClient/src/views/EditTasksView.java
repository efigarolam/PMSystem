    public void configure(int id_task) {
        this.translate();
        
        TasksBasicData[] tasks = this.tasksController.getAllTasks();
        if(id_task == 0) {
            this.preventDefault = true;
        } else {
            this.preventDefault = false;
        }
        
        jComboBox5.removeAllItems();
        if(tasks != null) {
            for(int i = 0; i < tasks.length; i++) {
                ComboBoxHelper cbbh = new ComboBoxHelper(tasks[i].id, tasks[i].title);
                jComboBox5.addItem(cbbh);
                if(id_task > 0) {
                    if(id_task == tasks[i].id) {
                        jComboBox5.setSelectedIndex(i);
                    }
                } else {
                    jComboBox5.setSelectedIndex(-1);
                }
            }
        }
        
        UsersBasicData[] users = this.usersController.getAllUsers();
        jList1.removeAll();
        DefaultListModel listModel = new DefaultListModel();
        if(users != null) {
            for(int i = 0; i < users.length; i++) {
                ComboBoxHelper cbbh = new ComboBoxHelper(users[i].id, users[i].username);
                listModel.addElement(users[i].username);
            }
        }
        jList1.setModel(listModel);
        
        this.preventDefault = false;
        jDateChooser1.setDateFormatString("yyyy-MM-dd");
        jDateChooser2.setDateFormatString("yyyy-MM-dd");
    }

